void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	k;

	i = 0;
	while (i < size)
	{
		k = tab[i];
		j = i - 1;
		while (j >= 0 && tab[j] > k)
		{
			tab[j + 1] = tab[j];
			j--;
		}
		i++;
		tab[j + 1] = k;
	}
}
