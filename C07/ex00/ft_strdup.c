#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strdup(char *src)
{
	char *array;
	int	i;
	int	len;

	len = ft_strlen(src) - 1;
	array = (char*)malloc(sizeof(char) * (len + 1));
	i = 0;
	while (i <= len)
	{
		array[i] = src[i];
		i++;
	}
	array[i] = '\0';
	return (array);
}
